"""
    2 - Faça um Programa que peça as 4 notas bimestrais e mostre a média.
"""

nota_01 = float(input('Digite a nota do 1º bimestre: '))
nota_02 = float(input('Digite a nota do 2º bimestre: '))
nota_03 = float(input('Digite a nota do 3º bimestre: '))
nota_04 = float(input('Digite a nota do 4º bimestre: '))

media = (nota_01 + nota_02 + nota_03 + nota_04) / 4

print(media)
